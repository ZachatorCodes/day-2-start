# Round takes in a value and how many decimal points to round it to
print(round((8 / 3), 2))

# Floor division: chops off the decimal (doesn't round)
print(8 // 3)

score = 0
score += 1  # Adds one to score

height = 10.6
weight = 3719.572
trueOrFalse = True

# F String - A way to interpolate strings with auto type casting

print(f"Your score is {score}. Your height and weight are {height} / {weight} respectively. The fact that you are a human is {trueOrFalse}.")
